			<CENTER>NIT Jamshedpur offers B. Tech. , M. Tech. , and Ph. D. programmes in Computer Science where B.Tech is the undergraduate programme , M.Tech & Ph.D are post-graduate programmes. The details of these programmes are provided below . Read and find out what you need.</CENTER><br>

			UNDERGRADUATE PROGRAMME (B. TECH)<br><br><br>
		
			The B.Tech Programme consists of 8 semesters and is a professional engineering degree affiliated with and awarded after completion of 4-years of extensive/vast engineering study and research. Computer science and engineering students will learn about basic engineering techniques, as other engineering students, during first year course, which includes Engineering Mathematics, Physics , Chemistry, Electrical Engineering, Electronics Engineering , Engineering Mechanics , Mechanical Activities( Welding, Fitting , Carpentry ,etc.) , Engineering drawing and Basic Programming. From second year they are exposed to core computer science subjects like Data Structures, Applied Graph Theory , Computer Organization and Architecture , Computer Graphics , Microprocessors and Peripherals , Operating Systems , Software Engineering , Compiler Design are few to mention. This programme is one of the most popular and demanded course , not only in India but in the world .<br><br><br>
			POSTGRADUATE PROGRAMME (M. TECH.)<br><br><br>
			The M.Tech programme in Computer Science and Engineering provides advanced knowledge in various areas of computer science. The programme is designed such that a student can complete it based on advanced coursework alone. However, the students are given the option to concentrate on a problem by substituting some of the courses with equivalent project work.
			The standard M. Tech. is a two year programme, where the first two semesters are spent doing courses. A one-year project commences in end of the 2nd semester . The project typically has a strong research component.<br><br><br>
			DOCTOR OF PHILOSOPHY (PH. D.)<br><br><br>
			The Ph.D. programme is a post-graduate programme focussed on original research, that can lead to publications in premier conferences or journals. It is the highest level of degree that a student can achieve. Students are required to do some courses in the first few semesters (course work required can vary based on student background), but soon move on completely to research. Many Ph.D scholars teaches B.tech students also and many labs are run by them. After completing the programme the scholar is capable of understanding significant particulars of computer system.<br><br><br>



			
